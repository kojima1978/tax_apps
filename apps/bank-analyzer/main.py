import streamlit as st

st.set_page_config(
    page_title="相続税通帳分析システム",
    page_icon="💰",
    layout="wide"
)

st.title("💰 相続税通帳分析システム")

st.markdown("""
### ようこそ

このシステムは、相続税申告における通帳調査を支援するツールです。

#### 使い方
1. 左側のサイドバーから **「案件一覧」** を選択し、分析対象の案件（被相続人名など）を作成または選択してください。
2. **「CSVインポート」** で、OCR変換済みの通帳データを取り込みます。
3. **「分析・表示」** で、資金移動や多額入出金を確認します。
4. **「設定」** で、検出閾値や分類パターンをカスタマイズできます。

#### 主な機能
- 📊 **資金移動の自動検出**: 口座間の資金移動を自動で検知
- 💰 **多額取引の抽出**: 設定した閾値以上の取引を自動抽出
- 🤖 **AI/ルールベース分類**: 取引を自動分類（生活費、資産形成、贈与疑い等）
- ⚙️ **柔軟な設定**: 閾値や分類パターンをカスタマイズ可能

#### 注意事項
- データは全てローカル(`data/`フォルダ)に保存されます。
- インターネットへのデータ送信は一切行われません（Ollama使用時を除く）。
""")
