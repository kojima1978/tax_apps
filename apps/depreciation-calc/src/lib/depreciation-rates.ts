export type DepreciationRates = {
    straightLine: number;
    db200: number;
    db200Revised: number;
    db200Guarantee: number;
    db250: number;
    db250Revised: number;
    db250Guarantee: number;
    oldStraightLine: number;
    oldDeclining: number;
};

/**
 * 耐用年数から償却率を取得する
 * 耐用年数2〜50年はNTA公表値、51年以上は計算値
 */
export function getDepreciationRates(usefulLife: number): DepreciationRates {
    if (usefulLife < 2) {
        throw new Error('耐用年数は2年以上である必要があります');
    }

    if (usefulLife <= 50) {
        const row = RATE_TABLE[usefulLife - 2];
        return {
            straightLine: row[0],
            db200: row[1],
            db200Revised: row[2],
            db200Guarantee: row[3],
            db250: row[4],
            db250Revised: row[5],
            db250Guarantee: row[6],
            oldStraightLine: row[7],
            oldDeclining: row[8],
        };
    }

    // 51年以上は計算で近似
    const n = usefulLife;
    const sl = Math.round(1 / n * 1000) / 1000;
    const db200 = Math.round(2 / n * 1000) / 1000;
    const db250 = Math.round(2.5 / n * 1000) / 1000;
    const oldSl = Math.round(0.9 / n * 1000) / 1000;
    const oldDb = Math.round((1 - Math.pow(0.1, 1 / n)) * 1000) / 1000;

    return {
        straightLine: sl,
        db200,
        db200Revised: db200,
        db200Guarantee: 0.01,
        db250,
        db250Revised: db250,
        db250Guarantee: 0.01,
        oldStraightLine: oldSl,
        oldDeclining: oldDb,
    };
}
